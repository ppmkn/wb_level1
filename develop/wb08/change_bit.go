package main

import (
	"fmt"
)

func main() {
	// Создаем переменную типа int64
	n := int64(42) // Присваиваем значение, например 42, в двоичном виде это: 101010

	// Номер бита, который нужно установить (исходя из того, что нумерация с нуля)
	i := 2

	// Устанавливаем i-й бит в 1
	n |= 1 << i

	// Выводим результат
	fmt.Printf("Число с установленным i-м битом в 1: %d\n", n) // Результат: 46 (в двоичном виде: 101110)

	// Устанавливаем i-й бит в 0
	n &^= 1 << i

	// Выводим результат
	fmt.Printf("Число с установленным i-м битом в 0: %d\n", n) // Результат: 42 (в двоичном виде: 101010)
}
